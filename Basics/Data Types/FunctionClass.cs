namespace DataTypes
{
    public class FunctionClass
    {
        /// <summary>
        /// Adding of two integers
        /// </summary>
        /// <param name="number1"></param>
        /// <param name="number2"></param>
        /// <returns></returns>
        public int AddingIntegers(int number1, int number2)
        {
            return number1 + number2;
        }
        public string AddingStrings(string string1,string string2)
        {
            return string1 + " " + string2;
        }
    }
}